package com.rcdiamondgh.utils;

public class ObjDataSaver {

    public float firstPersonRotatoX;
    public float firstPersonRotatoY;
    public float firstPersonRotatoZ;
    public float firstPersonTranslateX;
    public float firstPersonTranslateY;
    public float firstPersonTranslateZ;
    public float thirdPersonRotatoX;
    public float thirdPersonRotatoY;
    public float thirdPersonRotatoZ;
    public float thirdPersonTranslateX;
    public float thirdPersonTranslateY;
    public float thirdPersonTranslateZ;
    public float scaleX;
    public float scaleY;
    public float scaleZ;

    public ObjDataSaver(final float firstPersonRotatoX, final float firstPersonRotatoY, final float firstPersonRotatoZ,
        final float firstPersonTranslateX, final float firstPersonTranslateY, final float firstPersonTranslateZ,
        final float thirdPersonRotatoX, final float thirdPersonRotatoY, final float thirdPersonRotatoZ,
        final float thirdPersonTranslateX, final float thirdPersonTranslateY, final float thirdPersonTranslateZ,
        final float scaleX, final float scaleY, final float scaleZ) {
        this.firstPersonRotatoX = 0.0f;
        this.firstPersonRotatoY = 0.0f;
        this.firstPersonRotatoZ = 0.0f;
        this.firstPersonTranslateX = 0.0f;
        this.firstPersonTranslateY = 0.0f;
        this.firstPersonTranslateZ = 0.0f;
        this.thirdPersonRotatoX = 0.0f;
        this.thirdPersonRotatoY = 0.0f;
        this.thirdPersonRotatoZ = 0.0f;
        this.thirdPersonTranslateX = 0.0f;
        this.thirdPersonTranslateY = 0.0f;
        this.thirdPersonTranslateZ = 0.0f;
        this.scaleX = 1.0f;
        this.scaleY = 1.0f;
        this.scaleZ = 1.0f;
        this.firstPersonRotatoX = firstPersonRotatoX;
        this.firstPersonRotatoY = firstPersonRotatoY;
        this.firstPersonRotatoZ = firstPersonRotatoZ;
        this.firstPersonTranslateX = firstPersonTranslateX;
        this.firstPersonTranslateY = firstPersonTranslateY;
        this.firstPersonTranslateZ = firstPersonTranslateZ;
        this.thirdPersonRotatoX = thirdPersonRotatoX;
        this.thirdPersonRotatoY = thirdPersonRotatoY;
        this.thirdPersonRotatoZ = thirdPersonRotatoZ;
        this.thirdPersonTranslateX = thirdPersonTranslateX;
        this.thirdPersonTranslateY = thirdPersonTranslateY;
        this.thirdPersonTranslateZ = thirdPersonTranslateZ;
        this.scaleX = scaleX;
        this.scaleY = scaleY;
        this.scaleZ = scaleZ;
    }
}
